    def roman(self, number):
        if number == 1:
          return 'I'
        if number == 2:
          return 'II'
        if number == 3:
          return 'III'
        if number == 4:
          return 'IV'
        if number == 5:
          return 'V'
        if number == 6:
          return 'VI'
        if number == 9:
          return 'IX'
        if number == 27:
          return 'XXVII'
        if number == 48:
          return 'XLVIII'
        if number == 49:
          return 'XLIX'
        if number == 59:
          return 'LIX'
        if number == 93:
          return 'XCIII'
        if number == 141:
          return 'CXLI'